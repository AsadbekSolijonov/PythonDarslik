# Listlar o'zgaruvchan
my_list = [1, -3, 6, 7]
print(my_list)

# 1. append metodi - List ohiriga yangi element qo'shadi.
my_list.append('Python')
print(my_list)

# 2. insert metodi - Listning indeksi bo'yicha yangi element qo'shadi.
my_list.insert(-1, 'Abbos')
print(my_list)

# 3. remove metodi - Listning elementi bo'yicha olib tashlaydi
my_list.remove('Abbos')
print(my_list)

# 4. pop metodi - Listning indeksi bo'yicha elementni o'chiradi.
my_list.pop(0)
print(my_list)
