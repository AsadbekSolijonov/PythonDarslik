text = '    tOSh bOlTA oSHiq    '
# capitalized() - Bosh harfni doim katta harf qilib beradi.
# print(text.capitalize())
print(f"{text}.capitalize()  ==> {text.capitalize()}")
print(f"{text}.title()  ==> {text.title()}")
print(f"{text}.strip()  ==> {text.strip()}")
print(f"{text}.rstrip()  ==> {text.rstrip()}")
print(f"{text}.lstrip()  ==> {text.lstrip()}")
print(f"{text}.count('o')  ==> {text.count('o')}")
print(f"{text}.index('o')  ==> {text.index('o')}")
print(f"{text}.replace('O', 'a')  ==> {text.replace('O', 'a')}")
print(f"len({text}) ===> {len(text)}")

